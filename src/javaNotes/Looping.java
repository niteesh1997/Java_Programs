package javaNotes;

public class Looping {
	public static void main(String[] args) {
		int i=0, j=0;
		while(i<10) {
			System.out.println(i);
			i++;
	}
	
		do
		{
			System.out.println(j);
			j++;
		}
		while(j<=10);
		
		for(int k=0;k<=10;k++) {
			System.out.println(k);
		}
	}
}
